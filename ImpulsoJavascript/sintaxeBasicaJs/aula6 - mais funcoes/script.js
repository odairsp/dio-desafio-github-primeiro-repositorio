// tipos de funcao

// declarativa

function funcao() {
    console.log('Função declarativa!')
}
funcao();


// expressões de funções 

var funcao1 = function () { console.log('Expressão de função!') }
funcao1();

// Arrow function

var funcao2 = () => { console.log('Arrow function!') }
funcao2()

